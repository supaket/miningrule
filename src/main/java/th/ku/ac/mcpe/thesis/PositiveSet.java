package th.ku.ac.mcpe.thesis;

public class PositiveSet {
  private String freq;
  private String postiveBit;
  private int support;

  public String getFreq() {
    return freq;
  }

  public void setFreq(String freq) {
    this.freq = freq;
  }

  public String getPostiveBit() {
    return postiveBit;
  }

  public void setPostiveBit(String postiveBit) {
    this.postiveBit = postiveBit;
  }

  public int getSupport() {
    return support;
  }

  public void setSupport(int support) {
    this.support = support;
  }
}
